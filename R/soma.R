#' Soma
#'
#' Função que soma dois números.
#'
#' @param a (numeric) Um dos números para somar
#' @param b (numeric) O outro dos números para somar
#'
#' @return (numeric) Número resultante da soma de a + b
#'
#' @examples
#'
#' soma(3, 10)
#' soma(-2, 2)
#'
#' @export
soma <- function(a, b) {
  a + b
}


